<h1>Menü Reihenfolge ändern</h1>
<p>
	Die Reihenfolge der Blöcke ist die Reihenfolge, der Links, wie diese im Menü bzw. Submenü eingezeigt werden.<br/>
	Ziehe die Einzelnen Blöcke unter andere Seiten um diese als Submenü hinzuzufügen.<br/>
	Durchgestrichene Links, bezeichnen Seiten, welche nicht im Menü anzeigt werden.<br/>
	Einzelne Klicks auf die Links, führen zu den Seiten. Ein Klick auf das Stift Symbol führt zur Konfigurationsseite.<br/>
</p>
{menu_order}
<p class="box warning">
	Falls eine Seite (A) einer anderen Seite (B) zugeordnet wird und A ein Submenü hat, wird dieses in B nicht mehr angezeigt! Zur Zeit ist es also nicht möglich mehr als 1. Submenü pro Seite zu erstellen!<br/>
	Außerdem wird falls eine Seite, in das eigene Submenü gesetzt wird, dies nicht übernommen.
</p>